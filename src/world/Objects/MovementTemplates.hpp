    inline std::span<const MovementElement> getMovementTemplate(uint32_t internalOpcodeId)
    {
        switch (internalOpcodeId)
        {
        case MSG_MOVE_HEARTBEAT:
            return templateHeartbeat;
        case MSG_MOVE_START_FORWARD:
            return templateStartForward;
        case MSG_MOVE_STOP:
            return templateStop;
        case MSG_MOVE_FALL_LAND:
            return templateFallLand;
            // TODO: Add more cases for other movement opcodes and their corresponding templates

        default:
            return {};
        }
    }
